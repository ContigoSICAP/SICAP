package com.sicap.clientes.vo;


import java.io.Serializable;


public class TelefonoVO implements Serializable{

	public int idCliente;
	public int idDireccion;
	public int idTelefono;
	public int tipoTelefono;
	public String numeroTelefono;
        public String nomContacto;

	public TelefonoVO(){

		idCliente = 0;
		idDireccion = 0;
		idTelefono = 0;
		tipoTelefono = 0;
		numeroTelefono = null;

	}


	public String toString(){
		String respuesta = null;
		respuesta = "idCliente=["+idCliente+"],";
		respuesta += "idDireccion=["+idDireccion+"],";
		respuesta += "idTelefono=["+idTelefono+"],";
		respuesta += "tipoTelefono=["+tipoTelefono+"],";
		respuesta += "numeroTelefono=["+numeroTelefono+"]";
		return respuesta;
	}

}
