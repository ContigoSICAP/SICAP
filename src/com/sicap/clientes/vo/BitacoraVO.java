package com.sicap.clientes.vo;

import java.io.Serializable;
import java.sql.Timestamp;

public class BitacoraVO  implements Serializable{

	public int idCliente;
	public String usuario;
	public String comando;
	public Object objeto;
	public Timestamp fechaHora;


	public BitacoraVO (){
		idCliente = 0;
		usuario = null;
		comando = null;
		objeto = null;
		fechaHora = null;
	}
	//PROPIEDAD DE LA SOï¿½ICITUD


	public String toString(){
		String respuesta = null;
		respuesta = "idCliente=["+idCliente+"],";
		respuesta = "usuario=["+usuario+"],";
		respuesta += "comando=["+comando+"],";
		respuesta += "objeto=["+objeto+"],";
		respuesta += "fechaHora=["+fechaHora+"]";
		return respuesta;
	}

}
