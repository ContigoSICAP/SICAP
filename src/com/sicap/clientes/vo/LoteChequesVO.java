package com.sicap.clientes.vo;

import java.io.Serializable;
import java.sql.Date;

public class LoteChequesVO implements Serializable{

	public int idLote;
	public int idSucursal;
	public int idBanco;
	public int numChequeIni;
	public int numChequeFin;
	public Date fechaAlta;
	public Date fechaBaja;

	public LoteChequesVO(){

		idLote = 0;
		idSucursal = 0;
		idBanco = 0;
		numChequeIni = 0;
		numChequeFin = 0;
		fechaAlta = null;
		fechaBaja = null;
	}


	public String toString(){
		String respuesta = null;
		respuesta = "idLote=["+idLote+"],";
		respuesta = "idSucursal=["+idSucursal+"],";
		respuesta += "idBanco=["+idBanco+"],";
		respuesta += "numChequeIni=["+numChequeIni+"],";
		respuesta += "numChequeFin=["+numChequeFin+"],";
		respuesta += "fechaAlta=["+fechaAlta+"]";
		respuesta += "fechaBaja=["+fechaBaja+"]";
		return respuesta;
	}

}
