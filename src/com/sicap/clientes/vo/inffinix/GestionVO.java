package com.sicap.clientes.vo.inffinix;

import java.io.Serializable;
import java.sql.Date;

public class GestionVO implements Serializable{
	
	public String idClienteT24;
	public String contrato;
	public String codigoAccion;
	public String codigoResultado;
	public String codigoCarta;
	public Date fechaCaptura;
	public int numEjecutivo;
	public String comentario;
	
	public GestionVO(){
		idClienteT24 = null;
		contrato = null;
		codigoAccion = null;
		codigoResultado = null;
		codigoCarta = null;
		fechaCaptura = null;
		numEjecutivo = 0;
		comentario = null;
	}
	
	public String toString(){
		String respuesta = null;
		respuesta = "idClienteT24=[" + idClienteT24 + "],";
		respuesta += "contrato=[" + contrato + "],";
		respuesta += "codigoAccion=[" + codigoAccion + "],";
		respuesta += "codigoResultado=[" + codigoResultado + "],";
		respuesta += "codigoCarta=[" + codigoCarta + "],";
		respuesta += "fechaCaptura=[" + fechaCaptura + "],";
		respuesta += "numEjecutivo=[" + numEjecutivo + "],";
		respuesta += "comentario=[" + comentario + "]";
		return respuesta;
	}
}
