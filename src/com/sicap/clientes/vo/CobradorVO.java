package com.sicap.clientes.vo;

import java.sql.Timestamp;

public class CobradorVO {
    private int idCobrador;
    private int idSucursal;
    private String nombre;
    private String aMaterno;
    private String aPaterno;
    private int estatus;
    private String usuario;
    private Timestamp fechaHoramodificacion;
    
    public CobradorVO() {
        idCobrador = 0;
        idSucursal = 0;
        nombre = "";
        aPaterno = "";
        aMaterno = "";
        estatus = 0;
        usuario = "";
        fechaHoramodificacion = null;
    }
    public String toString() {
        String respuesta = "";
        respuesta = "idCobrador=[" + idCobrador + "],";
        respuesta += "idSucursal=[" + idSucursal + "],";
        respuesta += "nombre=[" + nombre + "],";
        respuesta += "aPaterno=[" + aPaterno + "],";
        respuesta += "aMaterno=[" + aMaterno + "],";
        respuesta += "estatus=[" + estatus + "],";
        respuesta += "usuario=[" + usuario + "],";
        respuesta += "fechaHoramodificacion=[" + fechaHoramodificacion + "],";

        return respuesta;
    }    

    public int getIdCobrador() {
        return idCobrador;
    }

    public void setIdCobrador(int idCobrador) {
        this.idCobrador = idCobrador;
    }

    public int getIdSucursal() {
        return idSucursal;
    }

    public void setIdSucursal(int idSucursal) {
        this.idSucursal = idSucursal;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getaMaterno() {
        return aMaterno;
    }

    public void setaMaterno(String aMaterno) {
        this.aMaterno = aMaterno;
    }

    public String getaPaterno() {
        return aPaterno;
    }

    public void setaPaterno(String aPaterno) {
        this.aPaterno = aPaterno;
    }

    public int getEstatus() {
        return estatus;
    }

    public void setEstatus(int estatus) {
        this.estatus = estatus;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public Timestamp getFechaHoramodificacion() {
        return fechaHoramodificacion;
    }

    public void setFechaHoramodificacion(Timestamp fechaHoramodificacion) {
        this.fechaHoramodificacion = fechaHoramodificacion;
    }
}
  