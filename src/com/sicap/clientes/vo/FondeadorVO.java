package com.sicap.clientes.vo;

/**
 *
 * @author ERojano
 */
public class FondeadorVO {
    
    private int numFondeador;
    private String nombre;
    private int preSeleccionCartera;
    private int estatus;
    private int prioridad;
    private int revolvente;
    private String razonSocial;
    
    public FondeadorVO(String nombre, int preseleccion, int estatus, int prioridad, int revolvente){
        this.nombre = nombre;
        this.preSeleccionCartera = preseleccion;
        this.estatus = estatus;
        this.prioridad = prioridad;
        this.revolvente = revolvente;
    }

    public int getNumFondeador() {
        return numFondeador;
    }

    public void setNumFondeador(int numFondeador) {
        this.numFondeador = numFondeador;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getPreSeleccionCartera() {
        return preSeleccionCartera;
    }

    public void setPreSeleccionCartera(int preSeleccionCartera) {
        this.preSeleccionCartera = preSeleccionCartera;
    }

    public int getEstatus() {
        return estatus;
    }

    public void setEstatus(int estatus) {
        this.estatus = estatus;
    }

    public int getPrioridad() {
        return prioridad;
    }

    public void setPrioridad(int prioridad) {
        this.prioridad = prioridad;
    }

    public int getRevolvente() {
        return revolvente;
    }

    public void setRevolvente(int revolvente) {
        this.revolvente = revolvente;
    }

    public String getRazonSocial() {
        return razonSocial;
    }

    public void setRazonSocial(String razonSocial) {
        this.razonSocial = razonSocial;
    }
  
}
