package com.sicap.clientes.vo;


import java.io.Serializable;


public class ColoniaVO implements Serializable{

	public int idColonia;
	public int idEstado;
	public int idMunicipio;
	public int cp;
	public String nombreColonia;
	public String asentamiento;

	public ColoniaVO(){

		idColonia = 0;
		idEstado = 0;
		idMunicipio = 0;
		cp = 0;
		nombreColonia = "";
		asentamiento = "";
	}


	public String toString(){
		String respuesta = null;
		respuesta = "idColonia=["+idColonia+"],";
		respuesta += "idEstado=["+idEstado+"],";
		respuesta += "idMunicipio=["+idMunicipio+"],";
		respuesta += "cp=["+cp+"],";
		respuesta += "nombreColonia=["+nombreColonia+"]";
		respuesta += "asentamiento=["+asentamiento+"]";
		return respuesta;
	}

}
