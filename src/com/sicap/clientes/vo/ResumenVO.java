package com.sicap.clientes.vo;

import java.io.Serializable;

public class ResumenVO implements Serializable{

	private String descripcion;
	private int    totalCuentas;
	private double limite;
	private double aprobado;
	private double actual;
	private double vencido;
	private int    pagoSemanal;
	private int    pagoQuincenal;
	private int    pagoMensual;
	
	public ResumenVO(String desc) {
		this.descripcion   = desc;
		this.totalCuentas  = 0;
		this.limite        = 0;
		this.aprobado      = 0;
		this.actual        = 0;
		this.vencido       = 0;
		this.pagoSemanal   = 0;
		this.pagoQuincenal = 0;
		this.pagoMensual   = 0;
	}

	public double getActual() {
		return actual;
	}

	public void setActual(double actual) {
		this.actual = actual;
	}

	public double getAprobado() {
		return aprobado;
	}

	public void setAprobado(double aprobado) {
		this.aprobado = aprobado;
	}

	public double getLimite() {
		return limite;
	}

	public void setLimite(double limite) {
		this.limite = limite;
	}

	public int getTotalCuentas() {
		return totalCuentas;
	}

	public void setTotalCuentas(int totalCuentas) {
		this.totalCuentas = totalCuentas;
	}

	public double getVencido() {
		return vencido;
	}

	public void setVencido(double vencido) {
		this.vencido = vencido;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public int getPagoMensual() {
		return pagoMensual;
	}

	public void setPagoMensual(int pagoMensual) {
		this.pagoMensual = pagoMensual;
	}

	public int getPagoQuincenal() {
		return pagoQuincenal;
	}

	public void setPagoQuincenal(int pagoQuincenal) {
		this.pagoQuincenal = pagoQuincenal;
	}

	public int getPagoSemanal() {
		return pagoSemanal;
	}

	public void setPagoSemanal(int pagoSemanal) {
		this.pagoSemanal = pagoSemanal;
	}
	
	
}
