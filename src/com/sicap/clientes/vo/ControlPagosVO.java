package com.sicap.clientes.vo;

import java.io.Serializable;
import java.sql.Date;

/**
 *
 * @author Alex
 */
public class ControlPagosVO implements Serializable{
    
    public int numPago;
    public Date fechaReunion;
    public Date fechaReal;
    public Date fechaPago;
    public int difDiasFechas;
    public double montoFichas;
    public double montoPagoCajas;
    public double difMonto;
    public double ahorroSemanal;
    public double saldoAhorro;

    public ControlPagosVO() {
    }

    public ControlPagosVO(int numPago, Date fechaReunion) {
        this.numPago = numPago;
        this.fechaReunion = fechaReunion;
    }

    public ControlPagosVO(int numPago, Date fechaReunion, Date fechaReal, Date fechaPago, int difDiasFechas, double montoFichas, double montoPagoCajas, double difMonto, double ahorroSemanal, double saldoAhorro) {
        this.numPago = numPago;
        this.fechaReunion = fechaReunion;
        this.fechaReal = fechaReal;
        this.fechaPago = fechaPago;
        this.difDiasFechas = difDiasFechas;
        this.montoFichas = montoFichas;
        this.montoPagoCajas = montoPagoCajas;
        this.difMonto = difMonto;
        this.ahorroSemanal = ahorroSemanal;
        this.saldoAhorro = saldoAhorro;
    }

    public double getAhorroSemanal() {
        return ahorroSemanal;
    }

    public void setAhorroSemanal(double ahorroSemanal) {
        this.ahorroSemanal = ahorroSemanal;
    }

    public int getDifDiasFechas() {
        return difDiasFechas;
    }

    public void setDifDiasFechas(int difDiasFechas) {
        this.difDiasFechas = difDiasFechas;
    }

    public double getDifMonto() {
        return difMonto;
    }

    public void setDifMonto(double difMonto) {
        this.difMonto = difMonto;
    }

    public Date getFechaPago() {
        return fechaPago;
    }

    public void setFechaPago(Date fechaPago) {
        this.fechaPago = fechaPago;
    }

    public Date getFechaReal() {
        return fechaReal;
    }

    public void setFechaReal(Date fechaReal) {
        this.fechaReal = fechaReal;
    }

    public Date getFechaReunion() {
        return fechaReunion;
    }

    public void setFechaReunion(Date fechaReunion) {
        this.fechaReunion = fechaReunion;
    }

    public double getMontoFichas() {
        return montoFichas;
    }

    public void setMontoFichas(double montoFichas) {
        this.montoFichas = montoFichas;
    }

    public double getMontoPagoCajas() {
        return montoPagoCajas;
    }

    public void setMontoPagoCajas(double montoPagoCajas) {
        this.montoPagoCajas = montoPagoCajas;
    }

    public int getNumPago() {
        return numPago;
    }

    public void setNumPago(int numPago) {
        this.numPago = numPago;
    }

    public double getSaldoAhorro() {
        return saldoAhorro;
    }

    public void setSaldoAhorro(double saldoAhorro) {
        this.saldoAhorro = saldoAhorro;
    }
        
}
