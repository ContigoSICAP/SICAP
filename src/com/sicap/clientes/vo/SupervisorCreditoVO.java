package com.sicap.clientes.vo;

import java.io.Serializable;
import java.sql.Timestamp;
//PROPIEDAD DE LA SOLICITUD

public class SupervisorCreditoVO implements Serializable{

	public int idSupervisor;
	public int idSucursal;
	public String nombre;
	public String aPaterno;
	public String aMaterno;
	
	public String estatus;
	public String usuario;
	public Timestamp fechaHoramodificacion;

	public SupervisorCreditoVO() {
		idSupervisor = 0;
		idSucursal = 0;
		nombre = null;
		aPaterno = null;
		aMaterno = null;
        estatus = null;
		usuario = null;
		fechaHoramodificacion = null;
	}
	
	public String toString(){
		String respuesta = null;
		respuesta = "idSupervisor=["+idSupervisor+"],";
		respuesta += "idSucursal=["+idSucursal+"],";
		respuesta += "nombre=["+nombre+"],";
		respuesta += "aPaterno=["+aPaterno+"],";
		respuesta += "aMaterno=["+aMaterno+"],";
		respuesta += "estatus=["+estatus+"],";
		respuesta += "usuario=["+usuario+"],";
		respuesta += "fechaHoramodificacion=["+fechaHoramodificacion+"],";

		return respuesta;
	}

}
