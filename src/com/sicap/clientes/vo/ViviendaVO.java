package com.sicap.clientes.vo;

import java.io.Serializable;
import java.sql.Timestamp;

public class ViviendaVO  implements Serializable{


	public int idCliente;
	public int idSolicitud;
	public int estatus;
	public int tipoVivienda;
	public double impAlquilerHipoteca;
	public int nivelVivienda;
	public int zona;
	public int pisos;
	public int cuartos;
	public int fachada;
	public int techo;
	public int tiempoResidencia;
	public Timestamp fechaCaptura;


	public ViviendaVO (){
		idCliente = 0;
		idSolicitud = 0;
		estatus = 0;
		tipoVivienda = 0;
		impAlquilerHipoteca = 0;
		nivelVivienda = 0;
		zona = 0;
		pisos = 0;
		cuartos = 0;
		fachada = 0;
		techo = 0;
		tiempoResidencia = 0;
		fechaCaptura = null;
	}


	public String toString(){
		String respuesta = null;
		respuesta = "idCliente=["+idCliente+"],";
		respuesta = "idSolicitud=["+idSolicitud+"],";
		respuesta += "estatus=["+estatus+"],";
		respuesta += "tipoVivienda=["+tipoVivienda+"],";
		respuesta += "impAlquilerHipoteca=["+impAlquilerHipoteca+"],";
		respuesta += "nivelVivienda=["+nivelVivienda+"],";
		respuesta += "zona=["+zona+"],";
		respuesta += "pisos=["+pisos+"],";
		respuesta += "cuartos=["+cuartos+"],";
		respuesta += "fachada=["+fachada+"],";
		respuesta += "techo=["+techo+"],";
		respuesta += "tiempoResidencia=["+tiempoResidencia+"],";
		respuesta += "fechaCaptura=["+fechaCaptura+"]";
		return respuesta;
	}

}
