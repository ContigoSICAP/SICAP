package com.sicap.clientes.vo;

import java.io.Serializable;
import java.sql.Date;
import java.sql.Timestamp;

public class PaynetVO implements Serializable{
    
    public int idPago;
    public String referenciaPay;
    public double monto;
    public Date fechaAutPagPay;
    public Date fechaAutMovPay;
    public Date fechaRevPagPay;
    public Date fechaRevMovPay;
    public int estatus;
    public String referencia;
    public String horaPago;
    public Timestamp fechaRegistro;
    public int numPago;
    public String tipoTran;
    public ReferenciaGeneralVO refGeneralVO;

    public PaynetVO() {
    }

    public PaynetVO(int idPago, String referenciaPay, double monto, Date fechaAutPagPay, Date fechaAutMovPay, int estatus) {
        this.idPago = idPago;
        this.referenciaPay = referenciaPay;
        this.monto = monto;
        this.fechaAutPagPay = fechaAutPagPay;
        this.fechaAutMovPay = fechaAutMovPay;
        this.estatus = estatus;
    }
    
    public PaynetVO(int idPago, String referenciaPay, double monto, Date fechaAutPagPay, Date fechaAutMovPay, int estatus, String tipoTran) {
        this.idPago = idPago;
        this.referenciaPay = referenciaPay;
        this.monto = monto;
        this.fechaAutPagPay = fechaAutPagPay;
        this.fechaAutMovPay = fechaAutMovPay;
        this.estatus = estatus;
        this.tipoTran = tipoTran;
    }

    public PaynetVO(int idPago, String referenciaPay, double monto, Date fechaAutPagPay, int estatus, String referencia, String horaPago) {
        this.idPago = idPago;
        this.referenciaPay = referenciaPay;
        this.monto = monto;
        this.fechaAutPagPay = fechaAutPagPay;
        this.estatus = estatus;
        this.referencia = referencia;
        this.horaPago = horaPago;
    }

    public PaynetVO(int idPago, String referenciaPay, double monto, Date fechaAutPagPay, Date fechaAutMovPay, int estatus, ReferenciaGeneralVO refGeneralVO) {
        this.idPago = idPago;
        this.referenciaPay = referenciaPay;
        this.monto = monto;
        this.fechaAutPagPay = fechaAutPagPay;
        this.fechaAutMovPay = fechaAutMovPay;
        this.estatus = estatus;
        this.refGeneralVO = refGeneralVO;
    }

    public PaynetVO(int idPago, String referenciaPay, int estatus) {
        this.idPago = idPago;
        this.referenciaPay = referenciaPay;
        this.estatus = estatus;
    }

    public int getIdPago() {
        return idPago;
    }

    public void setIdPago(int idPago) {
        this.idPago = idPago;
    }

    public String getReferenciaPay() {
        return referenciaPay;
    }

    public void setReferenciaPay(String referenciaPay) {
        this.referenciaPay = referenciaPay;
    }

    public double getMonto() {
        return monto;
    }

    public void setMonto(double monto) {
        this.monto = monto;
    }

    public Date getFechaAutPagPay() {
        return fechaAutPagPay;
    }

    public void setFechaAutPagPay(Date fechaAutPagPay) {
        this.fechaAutPagPay = fechaAutPagPay;
    }

    public Date getFechaAutMovPay() {
        return fechaAutMovPay;
    }

    public void setFechaAutMovPay(Date fechaAutMovPay) {
        this.fechaAutMovPay = fechaAutMovPay;
    }

    public Date getFechaRevPagPay() {
        return fechaRevPagPay;
    }

    public void setFechaRevPagPay(Date fechaRevPagPay) {
        this.fechaRevPagPay = fechaRevPagPay;
    }

    public Date getFechaRevMovPay() {
        return fechaRevMovPay;
    }

    public void setFechaRevMovPay(Date fechaRevMovPay) {
        this.fechaRevMovPay = fechaRevMovPay;
    }

    public int getEstatus() {
        return estatus;
    }

    public void setEstatus(int estatus) {
        this.estatus = estatus;
    }

    public String getReferencia() {
        return referencia;
    }

    public void setReferencia(String referencia) {
        this.referencia = referencia;
    }

    public String getHoraPago() {
        return horaPago;
    }

    public void setHoraPago(String horaPago) {
        this.horaPago = horaPago;
    }

    public Timestamp getFechaRegistro() {
        return fechaRegistro;
    }

    public void setFechaRegistro(Timestamp fechaRegistro) {
        this.fechaRegistro = fechaRegistro;
    }

    public int getNumPago() {
        return numPago;
    }

    public void setNumPago(int numPago) {
        this.numPago = numPago;
    }

    public String getTipoTran() {
        return tipoTran;
    }

    public void setTipoTran(String tipoTran) {
        this.tipoTran = tipoTran;
    }

    public ReferenciaGeneralVO getRefGeneralVO() {
        return refGeneralVO;
    }

    public void setRefGeneralVO(ReferenciaGeneralVO refGeneralVO) {
        this.refGeneralVO = refGeneralVO;
    }
    
}
