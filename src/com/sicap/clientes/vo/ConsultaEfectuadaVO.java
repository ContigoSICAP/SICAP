package com.sicap.clientes.vo;

import java.util.Date;

public class ConsultaEfectuadaVO {

	private int    idConsulta;
	private Date   fechaConsulta;
	private String otorgante;
	private String tipoCredito;
	private double monto;
	private String moneda;
	
	public ConsultaEfectuadaVO(){
		this.idConsulta    = 0;
		this.fechaConsulta = null;
		this.otorgante     = null;
		this.tipoCredito   = null;
		this.monto         = 0;
		this.moneda        = null;
	}

	public Date getFechaConsulta() {
		return fechaConsulta;
	}

	public void setFechaConsulta(Date fechaConsulta) {
		this.fechaConsulta = fechaConsulta;
	}

	public String getMoneda() {
		return moneda;
	}

	public void setMoneda(String moneda) {
		this.moneda = moneda;
	}

	public double getMonto() {
		return monto;
	}

	public void setMonto(double monto) {
		this.monto = monto;
	}

	public String getOtorgante() {
		return otorgante;
	}

	public void setOtorgante(String otorgante) {
		this.otorgante = otorgante;
	}

	public String getTipoCredito() {
		return tipoCredito;
	}

	public void setTipoCredito(String tipoCredito) {
		this.tipoCredito = tipoCredito;
	}

	public int getIdConsulta() {
		return idConsulta;
	}

	public void setIdConsulta(int idConsulta) {
		this.idConsulta = idConsulta;
	}
	
	
}
