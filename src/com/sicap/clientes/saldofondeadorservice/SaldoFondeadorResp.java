package com.sicap.clientes.saldofondeadorservice;
import java.util.Date;

/**
 *
 * @author LDAVILA
 */

public class SaldoFondeadorResp {
    private String status = null;
    private String errorCode;
    private String errorDetail;
    private Date timeStamp;
    private String SaldoFondeador;
    
    SaldoFondeadorResp(){
        status = "";
        errorCode = "";
        errorDetail = "";
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(String errorCode) {
        this.errorCode = errorCode;
    }

    public String getErrorDetail() {
        return errorDetail;
    }

    public void setErrorDetail(String errorDetail) {
        this.errorDetail = errorDetail;
    }

    public Date getTimeStamp() {
        return timeStamp;
    }

    public void setTimeStamp(Date timeStamp) {
        this.timeStamp = timeStamp;
    }

    public String getSaldoFondeador() {
        return SaldoFondeador;
    }

    public void setSaldoFondeador(String SaldoFondeador) {
        this.SaldoFondeador = SaldoFondeador;
    }
    
}
