package com.sicap.clientes.util.inffinix;

import java.sql.Date;

import com.sicap.clientes.util.FormatUtil;

public class FormatInffinixUtil extends FormatUtil{
	
	public static Date formatFechaInffinix(Date fechaInffinix){
		if( fechaInffinix!=null && fechaInffinix.toString().equalsIgnoreCase("1899-12-29") )
			return null;
		return fechaInffinix;
	}
}
